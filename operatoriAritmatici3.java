public class operatoriAritmatici3 {
    public static void main(String[] args) {
        int dato1 = 7;
        int dato2 = 5;
        int dato3 = 8;

        System.out.println(media(dato1, dato2, dato3));

    }

    public static int media(int dato1, int dato2, int dato3) {
        return (dato1 + dato2 + dato3) / 3;

    }
}